#include "ls.h"

int	main(int ac, char **av)
{
	t_ls_ctx ctx;

	if (!ls_ctor(&ctx, ac, av))
		ls_run(&ctx);
	ls_dtor(&ctx);
	return (EXIT_SUCCESS);
}
